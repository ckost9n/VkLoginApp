import UIKit

private let reuseIdentifier = "imageCell"

class PhotoCollectionViewController: UICollectionViewController {
    
//    let itemsPerRow: CGFloat = 2
//    let sectionInsert = UIEdgeInsets(top: 20, left: 20, bottom: 20, right: 20)
    
    lazy var service = VKService()
    
    var userId: String? = nil
    var photos: [Photo] = []

    override func viewDidLoad() {
        super.viewDidLoad()
        
        service.getPhoto(id: userId!) { [weak self] (photo) in
            self!.photos = photo
            self!.collectionView.reloadData()
        }

    }

    /*
    // MARK: - Navigation

    // In a storyboard-based application, you will often want to do a little preparation before navigation
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        // Get the new view controller using [segue destinationViewController].
        // Pass the selected object to the new view controller.
    }
    */

    // MARK: UICollectionViewDataSource
    
    override func numberOfSections(in collectionView: UICollectionView) -> Int {
        return 1
    }

    override func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return photos.count
    }

    override func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCell(withReuseIdentifier: "imageCell", for: indexPath) as! PhotoCell
    
        cell.configure(photo: photos[indexPath.row])
        
        return cell
    }

    // MARK: UICollectionViewDelegate

    /*
